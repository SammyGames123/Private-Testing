#!/usr/bin/env node
// Backfill venue Google Places linkage, latitude/longitude, and polished addresses
// via Google Places API (New).
//
// Usage:
//   GOOGLE_PLACES_API_KEY=... node scripts/backfill-venue-coordinates.js \
//       [--input /path/to/gold_coast_nightlife_claude_ready.json] \
//       [--out supabase/venues_backfill_coordinates.sql]
//
// Reads the Claude-ready JSON, calls the Places Text Search endpoint for each
// venue, and emits an idempotent SQL file of UPDATE statements keyed by slug.
// The generated SQL stores Google's stable place ID so future syncs can keep
// referencing the same place record instead of treating location enrichment as
// a one-off copy operation.

'use strict';

const fs = require('fs');
const path = require('path');

const DEFAULT_INPUT = '/Users/samsumsion/Downloads/gold_coast_nightlife_claude_ready.json';
const DEFAULT_OUT = path.resolve(__dirname, '..', 'supabase', 'venues_backfill_coordinates.sql');
const ENDPOINT = 'https://places.googleapis.com/v1/places:searchText';
const FIELD_MASK = 'places.id,places.location,places.formattedAddress,places.displayName';
const REQUEST_SPACING_MS = 120;
const MAX_RETRIES = 3;

function parseArgs(argv) {
    const out = { input: DEFAULT_INPUT, out: DEFAULT_OUT };
    for (let i = 2; i < argv.length; i += 1) {
        const flag = argv[i];
        const value = argv[i + 1];
        if (flag === '--input' && value) {
            out.input = value;
            i += 1;
        } else if (flag === '--out' && value) {
            out.out = value;
            i += 1;
        }
    }
    return out;
}

function sqlEscape(value) {
    if (value === null || value === undefined) return 'null';
    return `'${String(value).replace(/'/g, "''")}'`;
}

function sleep(ms) {
    return new Promise((resolve) => setTimeout(resolve, ms));
}

async function searchPlace(apiKey, query) {
    for (let attempt = 1; attempt <= MAX_RETRIES; attempt += 1) {
        const response = await fetch(ENDPOINT, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'X-Goog-Api-Key': apiKey,
                'X-Goog-FieldMask': FIELD_MASK,
            },
            body: JSON.stringify({ textQuery: query, regionCode: 'AU' }),
        });

        if (response.status === 429 || response.status >= 500) {
            const backoff = 500 * attempt;
            await sleep(backoff);
            continue;
        }

        if (!response.ok) {
            const text = await response.text();
            throw new Error(`Places API ${response.status}: ${text}`);
        }

        const body = await response.json();
        const first = body.places && body.places[0];
        if (!first || !first.id || !first.location) return null;
        return {
            placeId: first.id,
            latitude: first.location.latitude,
            longitude: first.location.longitude,
            formattedAddress: first.formattedAddress || null,
            displayName: first.displayName && first.displayName.text ? first.displayName.text : null,
        };
    }
    throw new Error('Retries exhausted');
}

async function main() {
    const apiKey = process.env.GOOGLE_PLACES_API_KEY;
    if (!apiKey) {
        console.error('Missing GOOGLE_PLACES_API_KEY env var.');
        process.exit(1);
    }

    const { input, out } = parseArgs(process.argv);
    const raw = fs.readFileSync(input, 'utf8');
    const parsed = JSON.parse(raw);
    const venues = Array.isArray(parsed.venues) ? parsed.venues : [];

    console.error(`Loaded ${venues.length} venues from ${input}`);

    const updates = [];
    const misses = [];

    for (let i = 0; i < venues.length; i += 1) {
        const venue = venues[i];
        const slug = venue.id;
        if (!slug || !venue.name) {
            misses.push({ slug, reason: 'missing id/name' });
            continue;
        }

        const query = [venue.name, venue.address, 'Gold Coast', 'Australia']
            .filter(Boolean)
            .join(', ');

        process.stderr.write(`[${i + 1}/${venues.length}] ${slug} … `);
        try {
            const result = await searchPlace(apiKey, query);
            if (!result) {
                process.stderr.write('no match\n');
                misses.push({ slug, reason: 'no match', query });
            } else {
                process.stderr.write(`${result.latitude.toFixed(5)}, ${result.longitude.toFixed(5)} (${result.placeId})\n`);
                updates.push({ slug, ...result });
            }
        } catch (err) {
            process.stderr.write(`error: ${err.message}\n`);
            misses.push({ slug, reason: err.message, query });
        }

        await sleep(REQUEST_SPACING_MS);
    }

    const header = [
        '-- Auto-generated by scripts/backfill-venue-coordinates.js',
        `-- Source: ${path.basename(input)}`,
        `-- Generated: ${new Date().toISOString()}`,
        `-- Hits: ${updates.length}  Misses: ${misses.length}`,
        '',
        'begin;',
        '',
    ].join('\n');

    const body = updates
        .map((u) => {
            const addressClause = u.formattedAddress
                ? `, address = coalesce(address, ${sqlEscape(u.formattedAddress)})`
                : '';
            return `update public.venues set google_place_id = ${sqlEscape(u.placeId)}, google_place_name = ${sqlEscape(u.displayName)}, google_last_synced_at = timezone('utc'::text, now()), latitude = ${u.latitude}, longitude = ${u.longitude}${addressClause} where slug = ${sqlEscape(u.slug)};`;
        })
        .join('\n');

    const footer = [
        '',
        '',
        'commit;',
        '',
    ].join('\n');

    const missesComment = misses.length
        ? '\n-- Misses (review manually):\n' + misses.map((m) => `-- ${m.slug}: ${m.reason}`).join('\n') + '\n'
        : '';

    fs.writeFileSync(out, header + body + footer + missesComment);
    console.error(`\nWrote ${updates.length} updates to ${out}`);
    if (misses.length) {
        console.error(`${misses.length} venues had no match — listed as comments at bottom of file.`);
    }
}

main().catch((err) => {
    console.error(err);
    process.exit(1);
});
